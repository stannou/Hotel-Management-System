    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        //Search by Rating Button Logic
        try {
            String name = "";
            int count = 0;
            if (jComboBoxSearchByCategory.getSelectedItem() != "Search By Rating") { // checking if the selected category is valid
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    if (jTable1.getValueAt(i, 1).toString().equals(jComboBoxSearchByCategory.getSelectedItem().toString())) {
                        count = count + 1;
                        name += jTable1.getValueAt(i, 0).toString() + ", ";
                    }
                }
                if (count == 0) {
                    JOptionPane.showMessageDialog(rootPane, "There are no available Hotel from " + jComboBoxSearchByCategory.getSelectedItem() + " Rating", "Available Hotel in this Rating", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "There are " + count + " " + jComboBoxSearchByCategory.getSelectedItem() + " available." + "\n" + "Their name are: " + name, "Available Hotel in this Rating", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "You must select a given Rating to continue", "Select an Item", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(rootPane, "Error", "Error!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

