    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        //Register to table
        try {
            String hotelname = jTextField2.getText().trim();
            String rating = (String) jComboBox1.getSelectedItem();
            String address = jTextField3.getText().trim();
            String price = jTextField4.getText().trim();
            String avalibility = jTextField5.getText().trim();
            Boolean flag = false;
            if (hotelname.equals("") || address.equals("") || price.equals("") || avalibility.equals("")) {
                JOptionPane.showMessageDialog(rootPane, " Please insert all value", "Empty Values", JOptionPane.ERROR_MESSAGE);
            } else {
                if (flag == false) {
                    int rowCount = jTable1.getRowCount();
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

                    boolean repeat = false;

                    if (rowCount == 0) {
                        //adding the inputs from user in the table

                        String data[] = {jTextField2.getText(), rating, jTextField3.getText(), jTextField4.getText(), jTextField5.getText()};
                        model.addRow(data);

                    } else {
                        for (int count = 0; count < rowCount; count++) {
                            String modelNumcheck = (String) jTable1.getValueAt(count, 0);
                            //checking if a model number of a appliances is already inserted or not
                            if (modelNumcheck.equals(hotelname)) {
                                repeat = true;
                                JOptionPane.showMessageDialog(rootPane, "Provided model number is already added", "Model number have been matched", JOptionPane.ERROR_MESSAGE);
                                break;
                            }
                        }
                        if (repeat == false) {
                            //adding the inputs from user in the table
                            String data[] = {jTextField2.getText(), rating, jTextField3.getText(), jTextField4.getText(), jTextField5.getText()};
                            model.addRow(data);
                            

                        }
                    }
                }
            
            }
            jButton9ActionPerformed(evt);
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Invalid Number!!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        


    }//GEN-LAST:event_jButton7ActionPerformed

